package br.estoque;

public class ClassificacaoMaterial {
	private String nome;
	private String codigo;
	
	public ClassificacaoMaterial() {}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
}
